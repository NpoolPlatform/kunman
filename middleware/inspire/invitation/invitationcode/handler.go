func WithInvitationCode(code *string, must bool) func(context.Context, *Handler) error {
	return func(ctx context.Context, h *Handler) error {
		if code == nil {
			if must {
				return wlog.Errorf("invalid code")
			}
			return nil
		}
		if len(*code) != codegenerator.InvitationCodeLen {
			return wlog.Errorf("invalid invitationcode")
		}
		h.InvitationCode = code
		return nil
	}
}

